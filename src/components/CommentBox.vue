<template>
  <div class="comment-box">
    <div class="comment-form">
      <input v-model="message" placeholder="请填写评论" />
      <button @click="submitComment">提交</button>
    </div>
    <div :key="comment.id" v-for="comment in reversedComments">
      {{ comment.body }}
    </div>
  </div>
</template>

<script>
  export default {
    name: 'CommentBox',
    data: () => ({
      message: ''  
    }),
    computed: {
      comments() { return this.$store.state.comment.all },
      reversedComments() { return this.comments.slice().reverse() }
    },
    methods: {
      submitComment () {
        let comment = {
          id: (this.comments.length + 1).toString(),
          body: this.message
        }
        this.$store.commit('addComment', comment)
        this.message = ''
      }
    }
  }
</script>

